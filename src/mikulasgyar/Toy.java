package mikulasgyar;

public class Toy {
    int cost;
    String color;
    String owner;
    int size;

    public Toy(String color) {
        this.cost = cost;
        this.color = color;
        this.owner = null;
    }
    public Toy(String color, int size) {
        this.cost = cost;
        this.color = color;
        this.owner = null;
        this.size = size;
    }
}
